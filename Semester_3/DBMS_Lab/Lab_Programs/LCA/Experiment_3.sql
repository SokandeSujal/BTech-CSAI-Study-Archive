-- Q1
SELECT COUNT(*) AS NumberOfEmployees, AVG(SAL) AS AverageSalary FROM EMP WHERE DEPTNO = 20;

-- Q2
SELECT ENAME, SAL, ((10 * SAL) / 100) AS PF FROM EMP;

-- Q3
SELECT EMPNO, ENAME, JOB, SAL FROM EMP ORDER BY SAL ASC;

-- Q4
SELECT ENAME, HIREDATE FROM EMP ORDER BY HIREDATE DESC;

-- Q5
SELECT ENAME AS Name, SAL AS Salary, ((10 * SAL) / 100) AS PF, ((50 * SAL) / 100) AS HRA, ((30 * SAL) / 100) AS DA, (SAL + (((10 * SAL) / 100)) + (((50 * SAL) / 100)) + ((30 * SAL) / 100)) AS GrossSalary FROM EMP ORDER BY GrossSalary ASC;

-- Q6
SELECT DEPTNO, COUNT(DEPTNO) FROM EMP GROUP BY DEPTNO;

-- Q7
SET SQL_SAFE_UPDATES = 0;
UPDATE EMP SET SAL = (SAL + (SAL * 0.10)) WHERE JOB = 'SALESMAN';

-- Q8
SELECT SUM(SAL) AS TotalSalary, MAX(SAL) AS MaximumSalary, MIN(SAL) AS MinimumSalary, AVG(SAL) AS AverageSalary FROM EMP WHERE DEPTNO = 20;

